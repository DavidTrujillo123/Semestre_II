    private void Datos(){
        int ejeMayor = Integer.parseInt(txtEjeMayor.getText());
        int ejeMenor = Integer.parseInt(txtEjeMenor.getText());
        int x = Integer.parseInt(txtX.getText());
        int y = Integer.parseInt(txtY.getText());
        
        if(this.eli == null){
            eli = new Elipse(x, y, ejeMayor, ejeMenor);
        }else{
            eli.setEjeMayor(ejeMayor);
            eli.setEjeMenor(ejeMenor);
            eli.setX(x);
            eli.setY(y);
        }
        
    }
