    private void btnMoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMoverActionPerformed
        // TODO add your handling code here:
        String cadena = "";
        Datos();
        
        cadena += eli.Imprimir();
        
        int dx = 0;
        int dy = 0;
        dx = Integer.parseInt(JOptionPane.showInputDialog("Introduce un numero para aumentar en X:"));
        dy = Integer.parseInt(JOptionPane.showInputDialog("Introduce un numero para aumentar en Y:"));
        
        eli.Mover(dx, dy);
        
        cadena += "\nNuevo centro:\n"+eli.Imprimir();
        this.Imprimir.setText(cadena);
        
        PonerDatos(eli.getX()+"", eli.getY()+"");
    }//GEN-LAST:event_btnMoverActionPerformed

    private void btnGraficarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGraficarActionPerformed
        // TODO add your handling code here:
        int ejeMayor = Integer.parseInt(txtEjeMayor.getText());
        int ejeMenor = Integer.parseInt(txtEjeMenor.getText());
        int x = Integer.parseInt(txtX.getText())+175;
        int y = 175 - Integer.parseInt(txtY.getText());
        
        if(this.eli == null){
            eli = new Elipse(x, y, ejeMayor, ejeMenor);
        }else{
            eli.setEjeMayor(ejeMayor);
            eli.setEjeMenor(ejeMenor);
            eli.setX(x);
            eli.setY(y);
            
        }
        
        g.drawOval((x-ejeMayor/2), (y-ejeMenor/2), ejeMayor, ejeMenor);
        
        g.drawLine(175, 0, 175, 350);
        g.drawLine(0, 175, 350, 175);
        
    }//GEN-LAST:event_btnGraficarActionPerformed

