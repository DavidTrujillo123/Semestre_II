    private void Datos(){
        int ladoMayor = Integer.parseInt(txtLadoMayor.getText());
        int ladoMenor = Integer.parseInt(txtLadoMenor.getText());
        int x = Integer.parseInt(txtX.getText());
        int y = Integer.parseInt(txtY.getText());
        
        if(this.rec == null){
            rec = new Rectangulo(x, y, ladoMayor, ladoMenor);
        }else{
            rec.setLadoMayor(ladoMayor);
            rec.setLadoMenor(ladoMenor);
            rec.setX(x);
            rec.setY(y);
        }
    }
