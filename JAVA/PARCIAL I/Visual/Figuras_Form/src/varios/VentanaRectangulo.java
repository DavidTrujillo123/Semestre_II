    private void btnAreaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAreaActionPerformed
        // TODO add your handling code here:
        if(this.p1 == null )this.p1 = new Punto(Integer.parseInt(txtX.getText()), Integer.parseInt(txtX.getText()));
        else{
            this.p1.setX(Integer.parseInt(txtX.getText()));
            this.p1.setY(Integer.parseInt(txtX.getText()));
        }
        
        int ladoMayor = Integer.parseInt(txtLadoMayor.getText());
        int ladoMenor = Integer.parseInt(txtLadoMenor.getText());
                
        if(this.rec == null){
            rec = new Rectangulo(p1.getX(), p1.getY(), ladoMayor, ladoMenor);
        }else{
            rec.setLadoMayor(ladoMayor);
            rec.setLadoMenor(ladoMenor);
            rec.setX(p1.getX());
            rec.setY(p1.getY());
        }
        this.Imprimir.setText(rec.Imprimir()+"\nArea:"+rec.CalcularArea());
    }//GEN-LAST:event_btnAreaActionPerformed

