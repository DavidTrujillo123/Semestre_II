package graficosFiguras;

/**
 *
 * @author Asus
 */
public class Punto {
    private int x;
    private int y;

    public Punto(int x, int y) {
        this.x = x;
        this.y = y;
    }
    public Punto(){
        this.x = 0;
        this.y = 0;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
    
    public void Mover(int dx, int dy){
        this.x += dx;
        this.y += dy;
    }
    
    public String Imprimir(){
        return "("+this.x+","+this.y+")";
    }
}
